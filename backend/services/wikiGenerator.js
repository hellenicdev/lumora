async function generateArchitecturePage(repositoryId, repo) {
  const nodes = await GraphNode.find({ repositoryId });
  const fileNodes = nodes.filter((n) => n.type === 'file');
  const routeNodes = nodes.filter((n) => n.type === 'route');
  const classNodes = nodes.filter((n) => n.type === 'class');

  let content = `# Architecture — ${repo.name || 'Project'}\n\n`;
  content += `## Overview\n\n${repo.description || 'Repository architecture overview.'}\n\n`;

  content += `## Project Structure\n\n`;
  content += `\`\`\`\n`;
  content += `${repo.name || 'project'}/\n`;

  const dirs = new Set();
  for (const node of fileNodes) {
    const parts = (node.name || '').split('/');
    if (parts.length > 1) {
      for (let i = 0; i < parts.length - 1; i++) {
        dirs.add('  '.repeat(i) + parts.slice(0, i + 1).join('/') + '/');
      }
    }
  }
  content += [...dirs].sort().join('\n');
  content += `\n\`\`\`\n\n`;

  if (routeNodes.length > 0) {
    content += `## API Routes\n\n`;
    content += `The following API routes were detected:\n\n`;
    for (const route of routeNodes) {
      content += `- \`${route.name}\`\n`;
    }
    content += '\n';
  }

  if (classNodes.length > 0) {
    content += `## Key Classes\n\n`;
    for (const cls of classNodes) {
      const extends_ = cls.metadata?.extends;
      content += `- **${cls.name}**${extends_ ? ` extends \`${extends_}\`` : ''}\n`;
    }
    content += '\n';
  }

  content += `---
*Architecture page auto-generated by Lumora AI.*`;

  return { title: 'Architecture', slug: 'Architecture', content, type: 'WIKI' };
}

async function generateAuthPage(repositoryId) {
  const endpoints = await ApiEndpoint.find({ repositoryId });
  const authEndpoints = endpoints.filter((e) =>
    e.path.toLowerCase().includes('auth') ||
    e.path.toLowerCase().includes('login') ||
    e.path.toLowerCase().includes('register')
  );

  let content = `# Authentication\n\n`;
  content += `## Overview\n\n`;
  content += `This section describes the authentication flow.\n\n`;

  if (authEndpoints.length > 0) {
    content += `## Auth Endpoints\n\n`;
    for (const ep of authEndpoints) {
      content += `### \`${ep.method} ${ep.path}\`\n`;
      content += `- **File:** \`${ep.file || 'Unknown'}\`\n`;
      if (ep.middleware && ep.middleware.length > 0) {
        content += `- **Middleware:** ${ep.middleware.join(', ')}\n`;
      }
      content += '\n';
    }
  } else {
    content += `No authentication-specific endpoints were detected.\n\n`;
  }

  content += `## Security Considerations\n\n`;
  content += `- Passwords should always be hashed\n`;
  content += `- Use JWT or session-based authentication\n`;
  content += `- Implement rate limiting on auth endpoints\n`;
  content += `- Validate all user input\n\n`;

  content += `---
*Auth documentation auto-generated by Lumora AI.*`;

  return { title: 'Authentication', slug: 'Authentication', content, type: 'WIKI' };
}

async function generateDatabasePage(repositoryId) {
  const nodes = await GraphNode.find({ repositoryId });
  const modelNodes = nodes.filter((n) => n.type === 'model');

  let content = `# Database\n\n`;
  content += `## Schema\n\n`;

  if (modelNodes.length > 0) {
    for (const model of modelNodes) {
      content += `### ${model.name}\n\n`;
      if (model.metadata?.fields) {
        content += `| Field | Type |\n|---|---|\n`;
        for (const [field, type] of Object.entries(model.metadata.fields)) {
          content += `| ${field} | ${type} |\n`;
        }
        content += '\n';
      }
    }
  } else {
    content += `No database models were detected through static analysis.\n\n`;
  }

  content += `---
*Database documentation auto-generated by Lumora AI.*`;

  return { title: 'Database', slug: 'Database', content, type: 'WIKI' };
}

async function generateApiReferencePage(repositoryId) {
  const endpoints = await ApiEndpoint.find({ repositoryId });

  let content = `# API Reference\n\n`;
  content += `## Endpoints\n\n`;

  if (endpoints.length === 0) {
    content += `No API endpoints were detected.\n`;
  } else {
    for (const ep of endpoints) {
      content += `### ${ep.method} \`${ep.path}\`\n\n`;
      content += `- **File:** \`${ep.file || 'Unknown'}\`\n`;
      if (ep.function) content += `- **Handler:** \`${ep.function}\`\n`;
      if (ep.middleware && ep.middleware.length > 0) {
        content += `- **Middleware:** ${ep.middleware.join(', ')}\n`;
      }
      content += '\n---\n\n';
    }
  }

  content += `---
*API reference auto-generated by Lumora AI.*`;

  return { title: 'API Reference', slug: 'API-Reference', content, type: 'WIKI' };
}

