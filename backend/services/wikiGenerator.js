async function generateDeploymentPage(repositoryId, repo) {
  let content = `# Deployment\n\n`;
  content += `## Production Setup\n\n`;

  const deps = await Dependency.find({ repositoryId });
  const hasDocker = deps.some((d) => d.name === 'docker' || d.ecosystem === 'system');

  if (hasDocker) {
    content += `### Docker\n\n`;
    content += `This project appears to use Docker:\n\n`;
    content += `\`\`\`bash\n`;
    content += `docker build -t ${repo.name || 'app'} .\n`;
    content += `docker run -p 3000:3000 ${repo.name || 'app'}\n`;
    content += `\`\`\`\n\n`;
  }

  content += `### Environment Variables\n\n`;
  content += `Ensure all required environment variables are set in production.\n\n`;

  content += `### Build\n\n`;
  content += `\`\`\`bash\n`;
  content += `npm run build\n`;
  content += `npm start\n`;
  content += `\`\`\`\n\n`;

  content += `---
*Deployment guide auto-generated by Lumora AI.*`;

  return { title: 'Deployment', slug: 'Deployment', content, type: 'WIKI' };
}
