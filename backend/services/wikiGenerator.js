export async function generateWiki(repositoryId) {
  const repo = await Repository.findById(repositoryId);
  if (!repo) throw new Error('Repository not found');

  const pages = await Promise.all([
    generateWikiHome(repositoryId, repo),
    generateArchitecturePage(repositoryId, repo),
    generateAuthPage(repositoryId),
    generateDatabasePage(repositoryId),
    generateApiReferencePage(repositoryId),
    generateDevelopmentGuide(repositoryId, repo),
    generateDeploymentPage(repositoryId, repo),
  ]);

  return pages;
}

